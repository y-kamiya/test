static const ll MOD = 1000000007;

ll a, n;

ll modpow(ll a, ll n, ll mod) {
    ll res = 1;
    while (n > 0) {
        if (n & 1) res = res * a % mod;
        a = a * a % mod;
        n >>= 1;
    }
    return res;
}

void _main() {
    cin >> a >> n;

    cout << modpow(a, n, MOD) << endl;
}

